def proctition_platinum(arr, *prcs)
    result = Hash.new { |k,v| k[v] = [] }
    prcs.each_index { |i| result[i+1] = [] }
    arr.each do |el|
        prcs.each_with_index do |prc, i|
            if prc.call(el)
                result[i + 1] << el
                break
            end
        end
    end
    result
end

