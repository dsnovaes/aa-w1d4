def procipher(sentence, hash)
    arr = sentence.split(" ")
    result = []
    arr.each do |ele|
        # debugger
        new_word = ele
        hash.each do |k,v|
            # debugger
            new_word = v.call(new_word) if k.call(ele)
        end
        result << new_word
    end
    result.join(" ")
end

