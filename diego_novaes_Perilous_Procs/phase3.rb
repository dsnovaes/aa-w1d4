def proc_suffix(sentence,hash)
    arr = sentence.split(" ")
    result = []
    arr.each do |x|
        temp = x
        hash.each do |k,v|
            # debugger
            if k.call(x)
                temp += hash[k]
            end
        end
        result << temp
    end
    result.join(" ")
end

