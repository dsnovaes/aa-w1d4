def picky_procipher(sentence,hash)
    arr = sentence.split(" ")
    arr.map! do |ele|
        new_word = ele
        hash.each do |k,v|
            # debugger
            if k.call(ele)
                new_word = v.call(new_word) 
                break
            end
            
        end
        new_word
    end
    arr.join(" ")

end


